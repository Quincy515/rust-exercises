#![allow(dead_code)]
use entity::dict;
use migration::DbErr;
use sea_orm::{ActiveModelTrait, Set};
use seaorm_demo::establish_connection;

#[tokio::main]
async fn main() -> Result<(), Box<dyn std::error::Error>> {
    create_dict(DICTS).await?;
    Ok(())
}

async fn create_dict(words: DictType) -> Result<(), DbErr> {
    let db = establish_connection().await?;
    for word in words {
        let dict = dict::ActiveModel {
            word: Set(String::from(word.0.trim())),
            translation: Set(String::from(word.1.trim())),
            ..Default::default()
        };
        dict.insert(&db).await?;
    }
    Ok(())
}

